    <h3 id="help1">About avenuevin</h3>
    <p>Avenuevin is a website for professionals, amateurs, and beginners! Everyone is invited ! Manage your wine
        tasting online, comment, share. Professional, fun, instructive…
        Notes can be taken rapidly thanks to the intuitive and interactive wine tasting form. 
        You can find where to buy or drink a wine around you. Bars, restaurants, cellars, 
        put online your business and enjoy the site's functionalities to be geolocalised and 
        be close to the wine amateurs
    </p>

    <h3 id="help2">How to Register</h3>
    <p>To get full access to the site, you need to be registered and logged in. If you haven't created a 
        user profile, register by clicking on the link on the welcome page. Once the registration form filled,
        you'll receive a confirmation email which includes a link to validate your profile. 
        If you do not receive an email, check your spam folder.
    </p>

