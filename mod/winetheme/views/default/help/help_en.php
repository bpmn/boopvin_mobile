    <h3 id="help4">Create the profile of a wine</h3>
    <p>If the search for a wine is unsuccessful, hit enter in the search bar.
        You will be then forwarded to a page which will allow you to create the profile of wine, 
        by clicking on the button "add a wine" at the bottom of the page. Only the fields: domain,
        country, appellation, region and varietals are required for the creation of the wine. 
        Be careful not to enter the millesimal when creating the wine profile, this will be done 
        during a wine tasting. For french, spanish, italian and american wines, let yourself be 
        guided by the propositions being displayed.
    </p>

