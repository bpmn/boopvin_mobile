    <h3 id="help5">Tasting a wine</h3>
    <p> Open the wine profile page that you'd like to taste (if the wine is not in the database, 
        please check the previous chapters to create a wine profile). Add the wine to your tasting club in 
        clicking on the button "add to my tasting club" located on the top right of the profile.
        Once the wine added to your club, the options "edit wine" and "taste" are available.
        Click on the button "taste" to display the wine tasting form. Only the millesimal, the entries
        in the form of buttons, and the rating are required to validate the tasting session.
        If you feel like it, you can also enter the aromas, the scents or your impressions in
        the corresponding fields. To view the tasting notes, click on the link "view the form".
    </p>
