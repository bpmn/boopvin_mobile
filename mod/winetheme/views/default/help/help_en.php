    <h3 id="help6">Follow a wine taster</h3>
    <p> You can follow a wine taster or an establishment to be noticed of their activities on the site 
        (tasting sessions, discussions, etc..). Click on the user name anywhere on the winsome to go to 
        their profile page. Click on "add to my contacts". To remove a contact, follow the same links, 
        and click on "remove from my contacts".
    </p>
    <h3 id="help7">Manage the profile of a bar, restaurant, cellar</h3><br>
    <h4 id="help71">Create an establishment profile</h4>
    <p>Only the users registered as professionals can create a page for an establishment.
        Open the main page for establishments by clicking on the main menu "Bars, restaurants, cellar…".
        Select the menu "add an establishment" on the right to access the profile creation page. 
        On the name, activity and address of the business are compulsory. The other fields give you 
        the possibility to give more information to the users.
        Do not forget to validate the address of your business by clicking "validate" to launch 
        the analysis of your address by the website, which will result by a pop up window opening and
        displaying your address on a google map. If the address is correct, click on "save" to 
        save it in the avenuevin database, so your business can be located by the users of avenuevin.
    </p>
    <h4 id="help72">Add a wine in your business cellar</h4>
    <p>Open the page of the wine you would like to add to your business cellar. On the menu on the right, click "add to my business". In the window which shows up, click 
        on the establisment(s) that you'd like to be associated with this wine 
        and validate with the button "add".
    </p>

    <h4 id="help73">Add a user to an establishment</h4>
    <p> You can add a member of avenuevin to your business so that person can manage also 
        the establishment page (add wines, add news…). Click on the user name from anywhere 
        on the website to access the user profile. Click on "add to my establishment".  
        At anytime you can remove a user from the members list of your establishment. 
        On the establishment page, on the right column, go through the action menus starting 
        on the icon of the member you would like to remove and click "remove from establishment"
    </p>

    <h3 id="help8">La géolocalisation</h3>
    <p>To enjoy the benefits of this functionality, you have to activate the localization
        module on your browser and accept to be localized when the question is asked. 
        On the profile of a wine, you will find suggestions on the right column where you can see where 
        to buy or drink the selected wine. Under the menu "bar, restaurants, cellars…" 
        you will find all the establishments around you, from where you are connected to Internet.
    </p>

