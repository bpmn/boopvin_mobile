    <h3 id="help3">How to Search for a wine</h3>
    <p> Use the search box labelled "search for a wine". The search works by keywords: vineyard names,
        appellation, region, country etc… the results are displayed as you type. Do not hesitate to 
        start again a search with different keywords if no names are shown. If the search is still unsuccessful
        or if the results list is too big, hit the enter key to validate your entry. You will then be forwarded
        to a webpage which will propose you the full list or to create the profile of a wine.
    </p>

