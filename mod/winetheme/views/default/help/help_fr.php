    <h3 id="help5">Déguster un vin</h3>
    <p>  Ouvrez la page du profil du vin que vous souhaitez déguster (si le vin n'est pas dans la base de donnée reportez vous au chapitres précedents pour créer le profil de vin). Ajoutez le vin à votre club de dégustation en cliquant sur le bouton
        "Ajouter à mon club dégustation" situé en haut à droite du profil. Une fois le vin ajouté à votre club de dégustation les options
        "Editer le vin" et "Déguster" vous seront proposées. Cliquez sur le bouton "Déguster" pour faire apparaitre la fiche de dégustation. Seuls le millésime, les entrées sous forme de boutons,
        et la note sont requis pour valider votre dégustation. Si vous vous sentez à l'aise dans l'exercice de la dégustation n'hésitez pas à préciser les arômes, la rétro-olfaction ou vos impressions dans les champs correspondants.
        Pour visualiser les dégustations des utilisateurs, cliquez sur le lien intitulé "voir la fiche".
    </p>
