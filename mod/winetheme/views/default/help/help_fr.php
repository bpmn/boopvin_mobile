    <h3 id="help6">Suivre un dégustateur</h3>
    <p> Vous pouvez suivre un dégustateur ou un établissement pour être averti de leurs activités sur le site (dégustations, discussions etc..)
        Cliquez sur le nom de l'utilisateur à partir de n'importe quelle page du site pour accéder à la page de son profil. Cliquez sur "Ajouter à mes contacts". Un racourci de cette action existe 
        en plaçant la souris sur la mini icone de l'utilisateur.
        A l'affichage d'une flèche cliquez dessus pour faire apparaitre un menu déroulant qui vous proposera différentes actions possibles concernant l'utilisateur concerné.
        A tout moment et en suivant les mêmes démarches décrites ci-dessus vous pouvez retirer un utilisateur de vos contacts en cliquant sur "retirer de mes contacts".
    </p>
    <h3 id="help7">Gérer le profil d'une cave, d'un bar à vin, d'un restaurant, d'un domaine...</h3><br>
    <h4 id="help71">Créer le profil d'un établissement</h4>
    <p>Seuls les utilisateurs enregistrés comme professionnel peuvent créer une page our un établissement. Ouvrez la page générale des établissements en cliquant sur le menu principal "Bars, restos,caves, domaines"
        sélectionnez le menu "ajouter un établissement" à droite pour accéder à la page de création du profil. Seuls le nom, l'activité et l'adresse
        de l'établissement sont nécessaire pour la validation. Les autres champs vous donnent la possibilité de compléter la description de l'activiter 
        pour mieux informer les internautes.<br>
        Une fois l'adresse de votre établissement saisie pensez à la valider en cliquant sur le bouton "valider" pour lancer l'analyse
        de l'adresse par le site. Une fenêtre "pop up" va s'ouvrir et afficher le détail de l'adresse.
        Si l'adresse détaillée affichée est correcte cliquez sur le bouton "enregistrer" pour mémoriser les coordonnées géographique de l'établissement dans la base de donnée d'avenuevin. .
        Cette démarche permet à votre établissement de bénéficier de la géolocalisation et de pouvoir être localisé par les utilisateurs d'avenuevin.


    </p>
    <h4 id="help72">Ajouter un vin dans la cave d'un de vos établissements</h4>
    <p>Ouvrez la page du vin que vous voulez ajouter à la cave de votre établissements. Dans le menu à droite de la page
        cliquez sur "ajouter à mes établissements". Dans la fenêtre qui s'affiche sélectionnez le (les) établissement(s) au(x)quel(s) vous désirez rattacher le vin puis valider avec le bouton "ajouter"</p>

    <h4 id="help73">Ajouter un membre à un établissement</h4>
    <p> Vous pouvez ajouter un membre d'avenuevin à vos établissements pour lui donner la possibilité de pouvoir également gérer l'établissement (rattacher des vins, actualiser les nouvelles etc..)
        Cliquez sur le nom de l'utilisateur à partir de n'importe quelle page du site pour accéder à la page de son profil. Cliquez sur "Ajouter à mes établissements". Un racourci de cette action existe 
        en plaçant la souris sur la mini icone de l'utilisateur.
        A l'affichage d'une flèche cliquez dessus pour faire apparaitre un menu déroulant qui vous proposera différentes actions possibles concernant l'utilisateur concerné.
        A tout moment vous pouvez retirer un utilisateur des membres de votre établissement. Sur la page de l'établissement concerné, dans la colonne de droite,
        déroulez les menus d'action à partir de l'icone du membre que vous voulez retirer et cliquez sur "retirer de l'établissement".
    </p>

    <h3 id="help8">La géolocalisation</h3>
    <p>Pour bénéficier des services de cette fonctionnalité vous devez activer le module localisation
        de votre navigateur et accepter d'être localisé lorsque la question vous est posée. 
        Dans la page du profil d'un vin, vous trouverez des suggestions dans la colonne de droite pour savoir où acheter ou consommer le vin en question.
        Sous l'onglet du menu principal "Les Bars, Restos, Caves, Domaines.." vous trouverez tous les établissements dans les alentours de votre point de connexion à internet.
    </p>
