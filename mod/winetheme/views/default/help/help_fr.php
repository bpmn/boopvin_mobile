    <h3 id="help1">Au sujet d'avenuevin</h3>
    <p>Avenuevin est un site de dégustations de vin pour pros, amateurs et débutants ! Tout le monde est convié aux festivités !
        Gérez vos dégustations en ligne, commentez , partagez. Pro, ludique, éducatif...Prise de notes rapide grâce à la fiche de dégustation intuitive et interactive.
        Trouvez où acheter ou consommer un vin dans vos alentours..
        Cavistes, bars, domaines, restaurants, mettez le profil de vos établissements en ligne et profitez
        des fonctionnalités du site pour être localisés et proches du consommateur.</p>

    <h3 id="help2">S'enregistrer</h3>
    <p>Pour avoir accès à la totalité du site vous devez être connecté. Si vous n'avez pas créer de profil
        utilisateur, enregistrez-vous en cliquant sur le lien dédié à cet effet sur la page d'accueil.
        Une fois le formulaire d'inscription rempli vous allez recevoir un email de confirmation contenant un lien pour valider votre profil.
        Si vous ne recevez aucun email, pensez à vérifier votre boîte de Spams.</p>

