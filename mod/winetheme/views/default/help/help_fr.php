    <h3 id="help4">Créer le profil d'un vin</h3>
    <p>
        Si la recherche d'un vin est infructueuse tapez sur la touche "entrée" au niveau de la 
        boite de recherche pour valider votre saisie. Vous serez alors redirigé vers une page qui vous proposera 
        d'enregistrer le profil d'un vin en cliquant sur le bouton intitulé "Ajouter un vin" en bas de la page.
        Seuls les champs: nom du domaine, pays, appellation, région et type sont requis pour l'enregistrement du vin. Attention ne saisissez pas le millésime lors de l'édition du profil du vin,  
        celui ci doit être renseigné lors de la dégustation.
        Pour les vins français, espagnols, italiens, américains laissez-vous guider par les propositions d'appellations lors de la saisie.
    </p>

