    <h3 id="help3">Rechercher un vin</h3>
    <p> Utiliser la boite de recherche intitulée "Chercher un vin". La recherche se fait par mots clés:
        noms du domaines, appellations, régions, pays (en anglais), noms de vignerons etc... Les résultats de la recherche sont affichés
        au fil de la saisie. N'hésitez pas à recommencer avec différents mots clés si aucune proposition ne
        s'affiche. Si la recherche est toujours infructueuse ou que la liste proposée est trop grande tapez sur la touche "entrée" au niveau de la 
        boite de recherche pour valider votre saisie.
        Vous serez alors redirigé vers une page qui vous proposera la liste dans son intégralité ou d'enregistrer le profil d'un vin.</p>

